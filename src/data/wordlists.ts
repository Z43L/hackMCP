export function getWordlistsByCategory(category: string): Wordlist[] {
  return wordlists.filter(w => w.category === category);
}

export function getWordlistById(id: string): Wordlist | undefined {
  return wordlists.find(w => w.id === id);
}

export function searchWordlists(query: string): Wordlist[] {
  const q = query.toLowerCase();
  return wordlists.filter(w => {
    return (
      w.name.toLowerCase().includes(q) ||
      w.category.toLowerCase().includes(q) ||
      w.description.toLowerCase().includes(q) ||
      w.tags.some(t => t.toLowerCase().includes(q)) ||
      w.words.some(word => word.toLowerCase().includes(q))
    );
  });
}
